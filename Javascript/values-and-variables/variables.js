// naming convention: Camel Case

// let: specific to scope. let is newer than var!
let age = 10;
age += 1;
age -= 1;
age *= 5;
age /= 5;
console.log("age = " + age);

let avgRating = 2.1;
console.log("Average Rating = " + avgRating);

let counter = 0;
counter++;
counter--;
console.log("Counter = " + counter);

// const: it is a constant
const chairs = 2;
console.log("Chairs = " + chairs);

// var: it is outdated! it is generic scope
var height = 100;
console.log("Height = " + height);
